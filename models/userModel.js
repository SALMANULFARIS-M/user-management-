const mongoose = require("mongoose"); // making mongoose(this is like mongo client)


//structuring database inserting information
const userSchema = new mongoose.Schema({

    name: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    mobile: {
        type: String,
        required: true
    },
    password: {
        type: String,
        required: true
    },
    is_admin: {
        type: Number,
        default: 0
    },
})



module.exports = mongoose.model('user', userSchema)